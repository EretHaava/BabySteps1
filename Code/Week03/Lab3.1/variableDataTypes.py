# What three functions can be used to get
# the integer, floating-point number or string
# version of a value?

# The int(), float(), and str() functions will evaluate 
# to the integer, floating-point number 
# and string versions of the value passed to them.

Age = 3             # int()
aGe = 3.5           # float()
agE = '3'           # str()
AGE = 'three'       # str()

print(type(Age))
print(type(aGe))
print(type(agE))
print(type(AGE))

